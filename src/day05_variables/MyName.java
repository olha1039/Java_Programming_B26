package day05_variables;

public class MyName {

    public static void main(String[] args) {

        char firstLetter = 'O';
        char secondLetter = 'l';
        char thirdLetter = 'h';
        char forthLetter = 'a';

        System.out.println(firstLetter);
        System.out.println(secondLetter);
        System.out.println(thirdLetter);
        System.out.println(forthLetter);

        System.out.println("" + firstLetter + secondLetter + thirdLetter + forthLetter);

        // or you can make a variable of the name

        String name = "" + firstLetter + secondLetter + thirdLetter + forthLetter;

        System.out.println(name);


    }


}
