package day08_relational_operators.homework_practice_08;

public class GallonsToLiters {
    public static void main(String[] args) {
        int gallon = 1;
        double liters = 3.78541;
        gallon = 10;
        System.out.println("10 gallons equal " + gallon * liters + " liters");
        gallon = 20;
        System.out.println("20 gallons equal " + gallon * liters + " liters");
    }
}
